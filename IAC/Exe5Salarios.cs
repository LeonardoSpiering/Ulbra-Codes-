using System;

class Exe5_AP1
{
	public static void Main()
	{
		char = r;
		int = ss;
		float s, t;
		
		do
		{
			Console.WriteLine("Digite um salario: ");
			s = float.Parse(Console.ReadLine());
			ss++
			t += s;
			
			Console.WriteLine("Voce deseja acrescentar mais salarios? (s - Sim / n - Nao): ");
			r = Console.ReadLine();
		}while(r == 's');
		
		Console.WriteLine("Foram salarios. Soma:  Media salarial: ", ss, t, t/ss)
	}
}